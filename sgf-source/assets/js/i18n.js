// Apply translations whenever language changes
(function applyI18n() {
  function translate(lang) {
    if (lang === 'en') {
      document.querySelectorAll('[data-i18n]').forEach(el => {
        const orig = el.getAttribute('data-i18n-original');
        if (orig !== null) el.textContent = orig;
      });
      return;
    }
    const dict = window.SGF_I18N[lang] || {};
    document.querySelectorAll('[data-i18n]').forEach(el => {
      const key = el.getAttribute('data-i18n');
      if (!el.hasAttribute('data-i18n-original')) {
        el.setAttribute('data-i18n-original', el.textContent);
      }
      if (dict[key]) el.textContent = dict[key];
    });
  }
  document.addEventListener('DOMContentLoaded', () => {
    translate(document.documentElement.getAttribute('data-lang') || 'en');
    new MutationObserver(() => {
      translate(document.documentElement.getAttribute('data-lang') || 'en');
    }).observe(document.documentElement, { attributes: true, attributeFilter: ['data-lang'] });
  });
})();
